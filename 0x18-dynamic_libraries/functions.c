#include "main.h"
/**
 *add - adds two numbers
 *@a: firsrt digit
 *@b: second digit
 *Return: sum
 */

int add(int a, int b)
{
	return (a + b);
}
/**
 *sub - subtracts one digit from another
 *@a: first digit
 *@b: second digit
 *Return: difference of the two numbers
 */

int sub(int a, int b)
{
	return (a - b);
}
/**
 *mul - multiplies two numbers
 *@a: first digit
 *@b: second digit
 *Return: product of two digits
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 *div - divide one digit from another
 *@a: first digit
 *@b: second digit
 *Return: division result
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 *mod - returns the remainder of the division of two digits
 *@a: first digit
 *@b: second digit
 *Return: remainder
 */
int mod(int a, int b)
{
	return (a % b);
}
